// Normal function
const add = function (x, y) {
    return x + y;
}

// Arrow function
// The function keyword is not being used
// ES6 standard
const addition = (x, y) => {
    return x + y;
}

const additionals = (x, y) => x + y;

console.log(add(2,3))
console.log(addition(4,6))
console.log(additionals(5,7))